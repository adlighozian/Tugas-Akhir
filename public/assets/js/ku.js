function filtersatu() {
    var tsatu = document.getElementById("tsatu");
    var tdua = document.getElementById("tdua");
    tsatu.classList.remove("hidden");
    tdua.classList.add("hidden");
}
function filterdua() {
    var tsatu = document.getElementById("tsatu");
    var tdua = document.getElementById("tdua");
    tdua.classList.remove("hidden");
    tsatu.classList.add("hidden");
}
